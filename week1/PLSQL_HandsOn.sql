CREATE OR REPLACE PROCEDURE UpdateSalary(
    p_EmployeeID IN NUMBER,
    p_NewSalary  IN NUMBER
)
AS
BEGIN
    UPDATE Employees
    SET Salary = p_NewSalary
    WHERE EmployeeID = p_EmployeeID;

    IF SQL%ROWCOUNT = 0 THEN
        INSERT INTO ErrorLog (ErrorMessage, LogDate)
        VALUES ('Employee not found', SYSDATE);

        COMMIT;
        RETURN;
    END IF;

    COMMIT;
END;
/

EXEC UpdateSalary(99, 80000);

SELECT * FROM ErrorLog;

CREATE TABLE AuditLog (
    AuditID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Message VARCHAR2(200),
    LogDate DATE
);

CREATE OR REPLACE PROCEDURE AddAuditLog(
    p_Message IN VARCHAR2
)
AS
BEGIN
    INSERT INTO AuditLog (Message, LogDate)
    VALUES (p_Message, SYSDATE);

    COMMIT;
END;
/

EXEC AddAuditLog('Salary updated successfully');

SELECT * FROM AuditLog;

CREATE OR REPLACE PROCEDURE ProcessMonthlyInterest
AS
BEGIN
    UPDATE Accounts
    SET Balance = Balance + (Balance * 0.01);

    COMMIT;
END;
/

EXEC ProcessMonthlyInterest;

